
public class VariableInJava {
	
	
	String name="sai";
	
	String name1="gdh";
	
	
	public void m1() {
		
		System.out.println(name);
		System.out.println(name1);
	}
	

	public static void main(String[] args) {
		
		VariableInJava j=new VariableInJava();
		System.out.println(j.name);	
		System.out.println(j.name1);
		
		
	}

}
